    <h1>Política de Privacidad y Protección de Datos</h1>
    <p><em>Última actualización: Febrero 2026</em></p>

    <p>En el <strong>Proyecto Intermodular "Conexión y Memoria" (Proyecto Estel)</strong>, nos tomamos muy en serio la privacidad. Esta aplicación ha sido desarrollada con fines educativos y de asistencia social, cumpliendo con las medidas de seguridad técnicas necesarias.</p>

    <h2>1. Responsable del Tratamiento</h2>
    <p>Los datos son gestionados por los administradores del sistema (Alumnos del ciclo ASI) con la única finalidad de demostrar la funcionalidad del proyecto académico.</p>

    <h2>2. Datos que recopilamos</h2>
    <ul>
        <li><strong>Datos de Registro:</strong> Nombre, correo electrónico y contraseña (esta última se almacena cifrada mediante hash seguro).</li>
        <li><strong>Datos del Paciente:</strong> Nombre y fase de la enfermedad (para adaptar la interfaz).</li>
        <li><strong>Datos Sensibles (Salud):</strong> Listado de medicamentos, citas médicas y fotografías personales subidas voluntariamente para las terapias de reminiscencia.</li>
    </ul>

    <h2>3. Finalidad y Legitimación</h2>
    <p>La base legal es el <strong>consentimiento explícito</strong> del usuario al registrarse. La finalidad es exclusivamente proporcionar las herramientas de agenda, recordatorios y estimulación cognitiva.</p>

    <h2>4. Seguridad de los Datos</h2>
    <p>Hemos implementado medidas de seguridad robustas, incluyendo:</p>
    <ul>
        <li>Cifrado de contraseñas (Bcrypt).</li>
        <li>Alojamiento en infraestructura segura (AWS) con Firewalls configurados.</li>
        <li>Restricción de acceso a archivos sensibles mediante reglas de servidor (.htaccess).</li>
        <li>Separación de credenciales mediante variables de entorno.</li>
    </ul>

    <h2>5. Tus Derechos</h2>
    <p>Como usuario, tienes derecho a Acceder, Rectificar, Cancelar (borrar) y Oponerte al tratamiento de tus datos. Para ejercer estos derechos o solicitar el borrado completo de tu cuenta, contacta con: <strong>equipo.estel4@gmail.com</strong>.</p>

